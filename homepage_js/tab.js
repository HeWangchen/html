function c3_tt_tab_left_over_l(){
	if($("c3_tt_left_body").css("display","none")){
		document.getElementById("c3_tt_left_body").style.display = "block";
		document.getElementById("c3_tt_left_body_1").style.display = "none";
		document.getElementById("c3_tt_left_head").style.backgroundImage="url(../homepage_image/3P/c3_tt_left_head.jpg)";
	}
	
}
